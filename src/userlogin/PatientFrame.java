    public String getPid(){
        String id = null;
        String pass = null;
        com.mysql.jdbc.PreparedStatement ps1;
        ResultSet rs;
        String query1 = "SELECT * from patient where username = ?";
        try {
            ps1 = (com.mysql.jdbc.PreparedStatement) DbConnection.getConnection().prepareStatement(query1);
     
            ps1.setString(1, uname);
            rs = ps1.executeQuery();
            if(rs.next()) {
               id = rs.getString("patient_id");
               pass = rs.getString("password");
               
            }
            
            DbConnection.getConnection().close();
        } catch (SQLException ex) {
            Logger.getLogger(PatientFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        return id;
    }
    
