     public boolean checkDate(String time,String id,String date){
        com.mysql.jdbc.PreparedStatement ps;
        ResultSet rs;
        boolean checktime = false;
        String query = "SELECT * FROM `appointment` WHERE `doctor_id` =?";
        
        try {
            ps = (com.mysql.jdbc.PreparedStatement) DbConnection.getConnection().prepareStatement(query);
            
            ps.setString(1, id);
            rs = ps.executeQuery();
            
            while(rs.next()) {
                if(time.equals(rs.getString("time")) && date.equals(rs.getString("date")))
                    checktime = true;
            }
            DbConnection.getConnection().close();
         } catch (SQLException ex) {
            Logger.getLogger(UserLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return checktime;
      
    }
     
    
