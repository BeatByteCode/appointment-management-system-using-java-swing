    private void jButton_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addActionPerformed
        String did = jTextField_id.getText();
        String time = jTimeChooser_time.getFormatedTime();
        String desp = jTextArea_textarea.getText();
        
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String date = df.format(jDateChooser_date.getDate());
        if(checkDate(time,did,date)){
            JOptionPane.showMessageDialog(rootPane, "doctor ID or schedule time not available");
        }else {
            com.mysql.jdbc.PreparedStatement ps;
            //ResultSet rs;
            
            int i;
            String query = "INSERT INTO `appointment`(`doctor_id`, `patient_id`, `date`, `time`, `description`) VALUES (?,?,?,?,?)";
            //String query1 = "SELECT patient_id from patient where username = ?";
            
            try {
                ps = (com.mysql.jdbc.PreparedStatement) DbConnection.getConnection().prepareStatement(query);
                //ps1 = (com.mysql.jdbc.PreparedStatement) DbConnection.getConnection().prepareStatement(query1);
                
                //ps1.setString(1, uname);
                //rs = ps1.executeQuery();
                //if(rs.next()) {
                   //pid = rs.getString("patient_id");
                //}
                ps.setString(1, did);
                ps.setString(2, pid);
                ps.setString(3, date);
                ps.setString(4, time);
                ps.setString(5, desp);
                i = ps.executeUpdate();
               
                if(i > 0){
                    JOptionPane.showMessageDialog(rootPane, "Appointment done");
                }
                DbConnection.getConnection().close();
            } catch (SQLException ex) {
                Logger.getLogger(PatientFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButton_addActionPerformed

